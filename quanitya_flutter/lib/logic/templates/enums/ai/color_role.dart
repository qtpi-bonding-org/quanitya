/// Enumeration of semantic color roles used in UI elements.
/// 
/// Each enum value represents a semantic purpose for colors within
/// UI widgets, allowing AI to assign palette colors to specific roles.
enum ColorRole {
  /// Primary color for main interactive elements (buttons, handles, etc.)
  primary,
  
  /// Secondary color for supporting elements (tracks, backgrounds, etc.)
  secondary,
  
  /// Text color for labels, input text, and readable content
  text,
  
  /// Background color for widget backgrounds and containers
  background,
  
  /// Border color for outlines, separators, and boundaries
  border,
}